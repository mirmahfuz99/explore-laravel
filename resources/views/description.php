<p><strong>General Overview</strong> </p>


	<p>GetDash is a ready-to-use web admin ui kit designed and developed in Flutter, which provides lots of pages and features to make your work easy.</p>


<h2>Key Features</h2>



<ul>

<li><strong>Flutter 3.13.5 </strong>(Latest)</li>
<li><strong>Fully Responsive</strong> design</li>
<li>Work with all devices (Desktop, Laptop, Tablet)</li>
<li>Light + Dark Mode</li>
<li>Multi Language + RTL Supported</li>
<li>State Management</li>
<li>Responsive Menu</li>

</ul>


